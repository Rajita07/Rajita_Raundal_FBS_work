//Template Literals
//A way to have embedded expressions and multi-line strings
//Using backticks (`) instead of single or double quotes
//`this is a template literal`

let name = "Rajita";
let age = 22;
//Using template literals to embed variables

let greeting = `Hello, my name is ${name} and I am ${age} years old.`;
console.log(greeting); // Output: Hello, my name is Rajita and I am 22 years old.

//Multi-line strings using template literals
let multiLineStr = `This is line 1.
This is line 2.
This is line 3.`;
console.log("\nMulti-line String:");
console.log(multiLineStr);
//Output:
//This is line 1.
//This is line 2.


