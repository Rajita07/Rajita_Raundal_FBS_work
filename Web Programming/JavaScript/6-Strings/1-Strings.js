//Strings
//String is a sequence of characters used to represent text.

//Creating Strings
//let str = "Rajita"
let str1 = "Hello, World!"; // Using double quotes
let str2 = 'JavaScript is fun.'; // Using single quotes
let str3 = `Template literals are awesome!`; // Using backticks


console.log("Created Strings:");
console.log(str1);
console.log(str2);
console.log(str3);

//-------------------------------------------------------------------------

//String Length
//str.length - gives the length of the string
console.log("\nString Length:");
console.log("Length of str1: " + str1.length); 
console.log("Length of str2: " + str2.length); // Accessing length property
console.log("Length of str3: " + str3.length);

//-------------------------------------------------------------------------

//String Indices
//str[index] - access character at specific index (0-based)
console.log("\nString Indices:");
console.log("First character of str1: " + str1[0]); // Accessing first character
console.log("Fifth character of str2: " + str2[4]); // Accessing fifth character
console.log("Last character of str3: " + str3[str3.length - 1]); // Accessing last character

